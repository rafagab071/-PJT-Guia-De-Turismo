          <h2>Sobre</h2>
          <p>O <strong>Chile</strong>, localizado na extremidade sul da América do Sul, é uma nação rica em diversidade geográfica e cultural. Com uma extensão territorial impressionante que se estende por mais de 4.300 quilômetros ao longo da costa oeste do continente, o país abrange uma variedade de paisagens, desde o deserto árido do Atacama até as majestosas montanhas da <strong>Cordilheira dos Andes</strong> e as exuberantes <strong>florestas da Patagônia</strong></p>
          <p>Aprofunde-se na rica herança cultural chilena ao explorar cidades como <strong>Valparaíso</strong>, com suas ruas coloridas e expressivas manifestações artísticas. <strong>Santiago</strong>, a capital, revela uma mistura equilibrada entre a arquitetura moderna e a preservação histórica, proporcionando uma compreensão mais profunda da evolução deste país ao longo do tempo.</p>
