          <br><br><h2>Geografia e Clima</h2>
          <p>O <strong>Chile</strong> exibe uma notável diversidade climática ao longo de sua extensão de mais de 4.000 quilômetros. Desde o deserto do Atacama, no norte, com <strong>temperaturas extremas</strong> e <strong>precipitação escassa</strong>, até a região central com <strong>clima mediterrâneo</strong> de verões secos e invernos suaves. Ao sul, na Patagônia, prevalece um <strong>clima oceânico</strong>, caracterizado por chuvas frequentes e temperaturas mais frias. A proximidade da Cordilheira dos Andes também influencia as variações climáticas, criando paisagens espetaculares. Essa diversidade faz do Chile um destino fascinante para quem busca experiências únicas em meio a cenários naturais distintos.</p>
