          <br><br><h2>Lugares para Visitar</h2>
          <div id="fotos2">
          <p>Plaza de Armas</p></div>
            <figure>
			        <img id="foto" src="./imagens/plaza.jpg" alt="imagem 02..." />
				    </figure>
              <p>A Plaza de Armas está situada no centro histórico de Santiago, cercada por importantes edifícios, como a majestosa Catedral Metropolitana e o imponente Palácio La Moneda. Esta praça tem sido o epicentro da vida social e política da cidade desde a sua fundação, em 1541, pelo conquistador espanhol Pedro de Valdivia. Ao longo dos séculos, a praça testemunhou inúmeros eventos e reviravoltas na história do Chile.</p>
          </div>
          <div id="fotos2">
          <p> Palacio la moneda</p></div>
            </p>
            <figure>
			        <img id="foto" src="./imagens/palacio.jpg" alt="imagem 02..." />
            </figure>
            <p>O Palácio La Moneda encontra-se na região central de Santiago, próximo à Plaza de Armas. Sua história remonta ao século XVIII, quando foi construído como uma casa da moeda durante o período colonial. Posteriormente, em 1846, o edifício foi transformado no Palácio do Governo, tornando-se a residência oficial do Presidente do Chile. O Palácio La Moneda é um exemplo magnífico da arquitetura neoclássica, com sua fachada grandiosa, colunas coríntias e detalhes meticulosos que exalam elegância. Uma das características mais marcantes é a praça diante do palácio, que é perfeitamente alinhada com a imponente fachada, proporcionando uma visão espetacular.</p>
          <div id="fotos2">
          <p>Museu Chileno de Arte Pré colombiana</p></div>
          <figure>  
            <img id="foto" src="./imagens/museu.jpg" alt="imagem 02..." />				
          </figure>
            <p>Situado no coração do centro histórico de Santiago, o Museu Chileno de Arte Pré-Colombiana ocupa um prédio histórico que, por si só, é um tesouro arquitetônico. O edifício foi originalmente construído no século XVI como um convento e preserva elementos coloniais que adicionam uma camada extra de história ao museu.</p>
          </div>
          <br>
          <div id="fotos2">
          <p>Mercado Central</p>
          </div>
          <figure>
            <img id="foto" src="https://a5k5v6q9.stackpathcdn.com/wp-content/uploads/2017/09/mercado-central-santiago-julho.jpg" alt="imagem 02..." />				
          </figure>
            <p>O Mercado Central está convenientemente localizado no centro de Santiago, às margens do Rio Mapocho, e é uma verdadeira instituição da cidade. Sua história remonta a 1872, quando foi inaugurado como um espaço dedicado ao comércio de produtos do mar. Ao entrar no Mercado Central, você será recebido por uma profusão de cores, aromas e sons. O mercado é um paraíso para os amantes de frutos do mar frescos. Bancas repletas de peixes, lagostas, caranguejos, ostras e outros tesouros do mar são exibidos com todo o esplendor.</p>

