          <h2>Sobre</h2>
          <p><strong>Santiago</strong>, a capital do <strong>Chile</strong>, é uma cidade vibrante e cosmopolita localizada no coração do país. Ela é o centro político, econômico e cultural do <strong>Chile</strong> e abriga mais de 7 milhões de habitantes em sua região metropolitana.</p>
          <p>A primavera e o outono são épocas ideais para visitar <strong>Santiago</strong> devido ao clima agradável e às condições de viagem mais tranquilas. Se você gosta de esportes de inverno, o inverno é uma ótima escolha.</p>
          <p>O verão é excelente para atividades ao ar livre, mas esteja preparado para mais turistas e preços mais elevados. Certifique-se de verificar as condições meteorológicas e considerar seus interesses pessoais ao planejar sua visita a <strong>Santiago</strong>.</p>
          <figure>
            <img id="foto" src="./imagens/cidade.jpg"/>
        </figure>
          <br><br><h2>Geografia e Clima</h2>
      <p><strong>Santiago</strong> está localizada em um vale cercado pela Cordilheira dos Andes, proporcionando paisagens espetaculares. No entanto, a geografia da cidade também resulta em problemas de poluição do ar, especialmente durante os meses de inverno. O clima é mediterrâneo, com invernos úmidos e verões secos e quentes. </p>
